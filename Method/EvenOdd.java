//Write a method to check whether a number is even or odd.
package com.number;

public class EvenOdd {
	public void checknumber() {
		int num1 = 10;
		if (num1 % 2 == 0) {
			System.out.println(num1 + " is " + "Even number");
		} else {
			System.out.println(num1 + " is " + "Odd number");
		}
	}
}
