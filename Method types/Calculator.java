//parameter with return type.
public class Calculator {
	public int printAdd(int a, int b) {
		// System.out.println("Addition");
		return a + b;
	}

	public int printsub(int a, int b) {
		return a - b;
	}

	public int printmultiply(int a, int b) {
		return a * b;
	}

	public int printdiv(int a, int b) {
		return a / b;
	}

	public int printmodulus(int a, int b) {
		return a % b;
	}
}
