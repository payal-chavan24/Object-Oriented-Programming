package com.nonparameterwithreturntype;
//6. Write a method to check whether a number is even or odd.
public class EvenOdd {
public String printEvenOdd() {
	int a=9;
	if(a%2==0) {
		return a+" is Even number ";//this is allowed to write like this
	}else {
		return a+" is odd number ";
	}
	
}
}
