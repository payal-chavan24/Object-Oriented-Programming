//10. Check Two Strings are Anagram or Not
//
//Example of Anagram:
//listen → silent
//
//Input:
//
//str1 = listen
//str2 = silent
//
//Output: Anagram
public class Anagram {
public static void main(String[] args) {
	String str="listen";
	boolean isAnagram=true;
	String str2="silent";
	if(str.length()!=str2.length()) {
		isAnagram=false;
		return;
	}
	
	
	
}
}
